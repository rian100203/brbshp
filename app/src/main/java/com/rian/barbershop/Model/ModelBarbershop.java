package com.rian.barbershop.Model;

public class ModelBarbershop {
    private String id, nama, foto, deskripsi, lokasi, koordinat;

    public String getId() {
        return id;
    }

    public String getNama() {
        return nama;
    }

    public String getFoto() {
        return foto;
    }

    public String getDeskripsi() {
        return deskripsi;
    }

    public String getLokasi() {
        return lokasi;
    }

    public String getKoordinat() {
        return koordinat;
    }
}
